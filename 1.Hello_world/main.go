package main

import "fmt" // import kha se aa raha h, y go ke standard library se aa raah h,

// fmt kya import kiya hai, y ek package import kiya hai.
// hmra knsa package hai? main package hai.
// ham main package ke andar fmt package ko import kr rhe h,

func main() {
	fmt.Println("Hello, World!") // fmt.Println kya hai? y ek function hai jo fmt package ke andar define kiya gya hai.
	// fmt.Println kya krta hai? y console pr output print krta hai.
	// "Hello, World!" kya hai? y ek string hai jo console pr print hoga.
	// hmne yha par main function ko define kiya hai, jo program ka entry point hai.
}
